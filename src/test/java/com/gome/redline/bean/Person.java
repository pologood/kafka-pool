package com.gome.redline.bean;

import com.gome.redline.model.KafkaMessageSendable;

/**
 * Created by Lanxiaowei at 2017/2/24 18:39
 */
public class Person implements KafkaMessageSendable {
    private int id;
    private String name;
    private int age;

    public Person() {}

    public Person(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
